export function WeeklyReport({ open, onClose, articles, portfolioPrices }: WeeklyReportProps) {
  const [copied, setCopied] = useState(false);

  const week = useMemo(() => getWeekRange(), []);

  // Filter articles to this week
  const weekArticles = useMemo(() => {
    const start = week.start.getTime();
    const end = week.end.getTime() + 86400000; // include end day
    return articles.filter((a) => {
      const t = new Date(a.publishedAt).getTime();
      return t >= start && t <= end;
    });
  }, [articles, week]);

  const totalArticles = weekArticles.length;

  const sourceCounts = useMemo(() => {
    const map = new Map<string, number>();
    weekArticles.forEach((a) => map.set(a.sourceName, (map.get(a.sourceName) || 0) + 1));
    return Array.from(map.entries())
      .map(([name, count]) => ({ name, count }))
      .sort((a, b) => b.count - a.count);
  }, [weekArticles]);

  const mostActiveDay = useMemo(() => {
    const dayMap = new Map<string, number>();
    weekArticles.forEach((a) => {
      const d = new Date(a.publishedAt).toLocaleDateString("ko-KR", { weekday: "long", month: "numeric", day: "numeric" });
      dayMap.set(d, (dayMap.get(d) || 0) + 1);
    });
    let max = 0;
    let maxDay = "-";
    dayMap.forEach((count, day) => {
      if (count > max) { max = count; maxDay = day; }
    });
    return maxDay;
  }, [weekArticles]);

  const readRate = totalArticles > 0 ? (weekArticles.filter((a) => a.isRead).length / totalArticles) * 100 : 0;
  const savedRate = totalArticles > 0 ? (weekArticles.filter((a) => a.isSaved).length / totalArticles) * 100 : 0;

  const topStories = useMemo(() => {
    return [...weekArticles]
      .filter((a) => a.isSaved)
      .sort((a, b) => new Date(b.publishedAt).getTime() - new Date(a.publishedAt).getTime())
      .slice(0, 5);
  }, [weekArticles]);

  const tagBreakdown = useMemo(() => {
    const map = new Map<string, number>();
    weekArticles.forEach((a) => a.tags.forEach((t) => map.set(t, (map.get(t) || 0) + 1)));
    return Array.from(map.entries())
      .map(([tag, count]) => ({ tag, count }))
      .sort((a, b) => b.count - a.count);
  }, [weekArticles]);

  const keywords = useMemo(() => {
    return extractKeywords(weekArticles.map((a) => a.title));
  }, [weekArticles]);

  const tagMax = tagBreakdown.length > 0 ? tagBreakdown[0].count : 1;

  const handleCopyMarkdown = useCallback(() => {
    const md = generateMarkdown(
      week.label, totalArticles, sourceCounts, mostActiveDay,
      readRate, savedRate, topStories, tagBreakdown, keywords, portfolioPrices
    );
    navigator.clipboard.writeText(md);
    setCopied(true);
    setTimeout(() => setCopied(false), 2000);
  }, [week, totalArticles, sourceCounts, mostActiveDay, readRate, savedRate, topStories, tagBreakdown, keywords, portfolioPrices]);

  const handleExportHTML = useCallback(() => {
    const html = generateHTML(
      week.label, totalArticles, sourceCounts, mostActiveDay,
      readRate, savedRate, topStories, tagBreakdown, keywords, portfolioPrices
    );
    const blob = new Blob([html], { type: "text/html;charset=utf-8" });
    const url = URL.createObjectURL(blob);
    const link = document.createElement("a");
    link.href = url;
    link.download = `ryzm-weekly-report-${new Date().toISOString().slice(0, 10)}.html`;
    link.click();
    URL.revokeObjectURL(url);
  }, [week, totalArticles, sourceCounts, mostActiveDay, readRate, savedRate, topStories, tagBreakdown, keywords, portfolioPrices]);

  const handlePrint = useCallback(() => {
    window.print();
  }, []);

  if (!open) return null;

  return (
    <div
      className="fixed inset-0 z-50 flex items-start justify-center pt-[4vh] overflow-y-auto"
      style={{ background: "rgba(0,0,0,0.5)", backdropFilter: "blur(4px)" }}
      onMouseDown={(e) => { if (e.target === e.currentTarget) onClose(); }}
    >
      <div
        className="w-full mb-8"
        style={{
          maxWidth: 800,
          background: "#0D0D0F",
          border: "1px solid #2D2D32",
          minHeight: 400,
        }}
      >
        {/* Header */}
        <div
          className="flex items-center justify-between px-6 py-4"
          style={{ borderBottom: "1px solid #2D2D32" }}
        >
          <div>
            <h1
              className="font-heading"
              style={{ color: "#C9A96E", fontSize: 20, letterSpacing: "0.08em", fontWeight: 700 }}
            >
              RYZM FINANCE WEEKLY REPORT
            </h1>
            <p style={{ fontFamily: "'Space Mono', monospace", fontSize: 12, color: "#8C8C91", marginTop: 2 }}>
              {week.label}
            </p>
          </div>
          <div className="flex items-center gap-2">
            <button
              onClick={handleCopyMarkdown}
              className="flex items-center gap-1.5 px-3 py-1.5 text-[11px] font-medium transition-colors"
              style={{
                border: "1px solid #2D2D32",
                background: copied ? "rgba(201,169,110,0.15)" : "transparent",
                color: copied ? "#C9A96E" : "#8C8C91",
              }}
            >
              {copied ? "복사됨" : "Markdown로 복사"}
            </button>
            <button
              onClick={handleExportHTML}
              className="flex items-center gap-1.5 px-3 py-1.5 text-[11px] font-medium transition-colors"
              style={{ border: "1px solid #2D2D32", background: "transparent", color: "#8C8C91" }}
            >
              HTML로 내보내기
            </button>
            <button
              onClick={handlePrint}
              className="flex items-center gap-1.5 px-3 py-1.5 text-[11px] font-medium transition-colors"
              style={{ border: "1px solid #2D2D32", background: "transparent", color: "#8C8C91" }}
            >
              인쇄
            </button>
            <button
              onClick={onClose}
              className="flex items-center justify-center w-7 h-7 text-[#8C8C91] hover:text-[#EBEBEB] transition-colors"
            >
              <svg width="14" height="14" viewBox="0 0 14 14" fill="none" stroke="currentColor" strokeWidth="1.5">
                <path d="M1 1l12 12M13 1L1 13" />
              </svg>
            </button>
          </div>
        </div>

        {/* Content */}
        <div className="px-6 py-5 space-y-6">
          {/* Overview Stats */}
          <div>
            <div
              className="font-heading"
              style={{
                fontSize: 10,
                fontWeight: 700,
                color: "#8C8C91",
                letterSpacing: "0.1em",
                textTransform: "uppercase" as const,
                marginBottom: 10,
                paddingBottom: 6,
                borderBottom: "1px solid #2D2D32",
              }}
            >
              OVERVIEW
            </div>
            <div style={{ display: "grid", gridTemplateColumns: "repeat(4, 1fr)", gap: 10 }}>
              {[
                { value: totalArticles, label: "총 기사" },
                { value: sourceCounts.length, label: "활성 소스" },
                { value: `${readRate.toFixed(0)}%`, label: "읽음률" },
                { value: `${savedRate.toFixed(0)}%`, label: "저장률" },
              ].map((s) => (
                <div
                  key={s.label}
                  style={{ background: "#1A1A1E", border: "1px solid #2D2D32", padding: "14px 16px" }}
                >
                  <div style={{ fontFamily: "'Space Mono', monospace", fontSize: 22, fontWeight: 700, color: "#C9A96E" }}>
                    {s.value}
                  </div>
                  <div style={{ fontSize: 11, color: "#8C8C91", marginTop: 2 }}>{s.label}</div>
                </div>
              ))}
            </div>
            <div style={{ marginTop: 8, fontSize: 11, color: "#8C8C91" }}>
              가장 활발한 날: <span style={{ color: "#EBEBEB", fontWeight: 600 }}>{mostActiveDay}</span>
            </div>
          </div>

          {/* Top Stories */}
          {topStories.length > 0 && (
            <div>
              <div
                className="font-heading"
                style={{
                  fontSize: 10,
                  fontWeight: 700,
                  color: "#8C8C91",
                  letterSpacing: "0.1em",
                  textTransform: "uppercase" as const,
                  marginBottom: 10,
                  paddingBottom: 6,
                  borderBottom: "1px solid #2D2D32",
                }}
              >
                TOP STORIES
              </div>
              {topStories.map((a, i) => (
                <div key={a.id} style={{ padding: "10px 0", borderBottom: "1px solid #2D2D32" }}>
                  <span style={{ fontFamily: "'Space Mono', monospace", color: "#C9A96E", fontSize: 12, fontWeight: 700 }}>
                    {String(i + 1).padStart(2, "0")}
                  </span>
                  <span style={{ fontSize: 13, fontWeight: 600, marginLeft: 8, color: "#EBEBEB" }}>{a.title}</span>
                  <div style={{ fontSize: 11, color: "#8C8C91", marginTop: 3, marginLeft: 24 }}>
                    {a.sourceName} · {new Date(a.publishedAt).toLocaleDateString("ko-KR")}
                  </div>
                </div>
              ))}
            </div>
          )}

          {/* Tag Breakdown */}
          {tagBreakdown.length > 0 && (
            <div>
              <div
                className="font-heading"
                style={{
                  fontSize: 10,
                  fontWeight: 700,
                  color: "#8C8C91",
                  letterSpacing: "0.1em",
                  textTransform: "uppercase" as const,
                  marginBottom: 10,
                  paddingBottom: 6,
                  borderBottom: "1px solid #2D2D32",
                }}
              >
                TAG BREAKDOWN
              </div>
              {tagBreakdown.map((t) => (
                <div key={t.tag} style={{ display: "flex", alignItems: "center", gap: 8, marginBottom: 5 }}>
                  <span style={{ fontSize: 11, width: 80, textAlign: "right", color: "#8C8C91" }}>{t.tag}</span>
                  <div
                    style={{
                      height: 14,
                      background: "rgba(201,169,110,0.25)",
                      borderRight: "2px solid #C9A96E",
                      width: Math.max(4, (t.count / tagMax) * 300),
                      transition: "width 0.3s",
                    }}
                  />
                  <span style={{ fontFamily: "'Space Mono', monospace", fontSize: 10, color: "#8C8C91" }}>{t.count}</span>
                </div>
              ))}
            </div>
          )}

          {/* Source Activity */}
          {sourceCounts.length > 0 && (
            <div>
              <div
                className="font-heading"
                style={{
                  fontSize: 10,
                  fontWeight: 700,
                  color: "#8C8C91",
                  letterSpacing: "0.1em",
                  textTransform: "uppercase" as const,
                  marginBottom: 10,
                  paddingBottom: 6,
                  borderBottom: "1px solid #2D2D32",
                }}
              >
                SOURCE ACTIVITY
              </div>
              {sourceCounts.map((s) => (
                <div key={s.name} style={{ display: "flex", alignItems: "center", gap: 8, marginBottom: 5 }}>
                  <span style={{ fontSize: 11, width: 120, textAlign: "right", color: "#8C8C91", overflow: "hidden", textOverflow: "ellipsis", whiteSpace: "nowrap" }}>
                    {s.name}
                  </span>
                  <div
                    style={{
                      height: 14,
                      background: "rgba(201,169,110,0.25)",
                      borderRight: "2px solid #C9A96E",
                      width: Math.max(4, (s.count / (sourceCounts[0]?.count || 1)) * 250),
                      transition: "width 0.3s",
                    }}
                  />
                  <span style={{ fontFamily: "'Space Mono', monospace", fontSize: 10, color: "#8C8C91" }}>{s.count}</span>
                </div>
              ))}
            </div>
          )}

          {/* Market Summary */}
          {portfolioPrices && portfolioPrices.length > 0 && (
            <div>
              <div
                className="font-heading"
                style={{
                  fontSize: 10,
                  fontWeight: 700,
                  color: "#8C8C91",
                  letterSpacing: "0.1em",
                  textTransform: "uppercase" as const,
                  marginBottom: 10,
                  paddingBottom: 6,
                  borderBottom: "1px solid #2D2D32",
                }}
              >
                MARKET SUMMARY
              </div>
              {portfolioPrices.map((p) => {
                const sign = p.change >= 0 ? "+" : "";
                const color = p.change >= 0 ? "#22C55E" : "#EF4444";
                return (
                  <div
                    key={p.symbol}
                    style={{ display: "flex", justifyContent: "space-between", padding: "7px 0", borderBottom: "1px solid #2D2D32", fontSize: 12 }}
                  >
                    <span style={{ fontWeight: 600 }}>{p.symbol}</span>
                    <span style={{ fontFamily: "'Space Mono', monospace" }}>
                      {p.price.toLocaleString()}{" "}
                      <span style={{ color }}>{sign}{p.changePct.toFixed(2)}%</span>
                    </span>
                  </div>
                );
              })}
            </div>
          )}

          {/* Trending Keywords */}
          {keywords.length > 0 && (
            <div>
              <div
                className="font-heading"
                style={{
                  fontSize: 10,
                  fontWeight: 700,
                  color: "#8C8C91",
                  letterSpacing: "0.1em",
                  textTransform: "uppercase" as const,
                  marginBottom: 10,
                  paddingBottom: 6,
                  borderBottom: "1px solid #2D2D32",
                }}
              >
                TRENDING KEYWORDS
              </div>
              <div style={{ display: "flex", flexWrap: "wrap", gap: 6 }}>
                {keywords.map((k) => (
                  <span
                    key={k.word}
                    style={{
                      background: "#1A1A1E",
                      border: "1px solid #2D2D32",
                      padding: "4px 10px",
                      fontSize: 11,
                      color: "#EBEBEB",
                    }}
                  >
                    {k.word}
                    <span style={{ fontFamily: "'Space Mono', monospace", color: "#C9A96E", fontSize: 10, marginLeft: 4 }}>
                      {k.count}
                    </span>
                  </span>
                ))}
              </div>
            </div>
          )}

          {/* Empty state */}
          {totalArticles === 0 && (
            <div style={{ textAlign: "center", padding: "40px 0", color: "#8C8C91", fontSize: 13 }}>
              이번 주 수집된 기사가 없습니다.
            </div>
          )}
        </div>
      </div>
    </div>
  );
}
